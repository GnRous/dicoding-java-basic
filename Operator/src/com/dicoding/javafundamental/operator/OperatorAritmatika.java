package com.dicoding.javafundamental.operator;

public class OperatorAritmatika {
    public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil 5 + 1 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 4 - 1;
        System.out.println("Hasil 4 - 1 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Perkalian");
        int hasilPerkalian = 5 * 5;
        System.out.println("Hasil 5 x 5 = " + hasilPerkalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasiPembagian = 20 / 2;
        System.out.println("Hasil 20 / 2 = " + hasiPembagian);
        System.out.println();

        System.out.println("Opeari habis Bagi");
        int hasilSisa = 8 % 3;
        System.out.println("Hasil 8 % 3 = " + hasilSisa);
        int hasilSisaLain = 9 % 2;
        System.out.println("Hasil 9 % 2 = " + hasilSisaLain);
        System.out.println();
    }
}
